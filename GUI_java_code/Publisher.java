    private void btnSetNewChiefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetNewChiefActionPerformed
        int reply = JOptionPane.showConfirmDialog(null, "Are you sure?");
        if(reply == JOptionPane.YES_OPTION){
            String sql = "SELECT * FROM diaxirizetai WHERE dia_onoma=?";
            String sql1 = "UPDATE dimosiografos SET dim_idiothta=? WHERE dim_email=?";
            String sql2 = "UPDATE login SET property=? WHERE email=?";
            String sql3 = "UPDATE diaxirizetai SET dia_email=? WHERE dia_onoma=?";
            try{
            String temp_email = "";
            //TAKE OLD CHIEFS EMAIL
            pst = conn.prepareStatement(sql);
            pst.setString(1, comp);
            rs = pst.executeQuery();
            if(rs.next()){
                temp_email = rs.getString("dia_email");
            }
            
            //SETS OLD CHIEF -----> JOURNALIST
            pst = conn.prepareStatement(sql1);
            pst.setString(1, "Dimosiografos");
            pst.setString(2, temp_email);
            pst.executeUpdate();
                    
            pst = conn.prepareStatement(sql2);
            pst.setString(1, "Journalist");
            pst.setString(2, temp_email);
            pst.executeUpdate();
            
            //SETS JOUNALIST -----> NEW CHIEF
            pst = conn.prepareStatement(sql1);
            pst.setString(1, "Arxisintakths");
            pst.setString(2, comboNewChief.getSelectedItem().toString());
            pst.executeUpdate();
            
            pst = conn.prepareStatement(sql2);
            pst.setString(1, "Chief");
            pst.setString(2, comboNewChief.getSelectedItem().toString());
            pst.executeUpdate();
            
            //SETS IT TO TABLE DIAXIRIZETAI
            pst = conn.prepareStatement(sql3);
            pst.setString(1, comboNewChief.getSelectedItem().toString());
            pst.setString(2, comp);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Successful Update!");
            setAndRefresh();
            
            
        }
        catch(Exception ex){JOptionPane.showMessageDialog(null, ex);}
            
        }
        
        
        
    }//GEN-LAST:event_btnSetNewChiefActionPerformed

