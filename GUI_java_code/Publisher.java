    private void btnSetNewNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetNewNameActionPerformed
        int reply = JOptionPane.showConfirmDialog(null, "Are you sure?");
        if (reply == JOptionPane.YES_OPTION){
        if(!txtNewName.getText().equals("")){
            String sql = "UPDATE efhmerida SET efh_onoma=? WHERE efh_onoma=?";
            
            try{
                //UPDATE TABLE EFHMERIDA
                pst = conn.prepareCall(sql);
                pst.setString(1, txtNewName.getText());
                pst.setString(2, comp);
                pst.executeUpdate();
                comp = txtNewName.getText();
                setTitle(comp);
                JOptionPane.showMessageDialog(null, "Successful change!");
                txtNewName.setText("");
                setAndRefresh();
            }
            catch(Exception ex){JOptionPane.showMessageDialog(null, "There cant be 2 NewsPaper with the same Name!");}
        }
        else{
            JOptionPane.showMessageDialog(null, "NewsPaper name can not be empty!");
        }
    }
    }//GEN-LAST:event_btnSetNewNameActionPerformed

